    public void fillBox() {
        usersBox.removeAllItems();
        warnLabel.setText("");
        List usersList = uM.getAllUsers();
        Iterator<User> iterator = usersList.iterator();
        while (iterator.hasNext()) {
            usersBox.addItem(iterator.next().getName());
        }
    }
