    public void doContinueGame() {
        if (session.getLoggedInUser() != null) {
            boolean gameOn = session.getLoggedInUser().isGameOn();
            if (gameOn == true) {
                ContinueGame.setVisible(true);
            } else {
                ContinueGame.setVisible(false);
            }
        }
    }

