    private void okButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_okButtonActionPerformed
        // TODO add your handling code here:
        String input = new String(oldPassword.getPassword());
        if (!pM.checkParentPassword(input)) {
            warnLabel.setText("Wrong password!");
            return;
        }
        String pass1 = new String(newPassword.getPassword());
        String pass2 = new String(newPassword1.getPassword());
        if (pass1.length() < 8) {
            warnLabel.setText("Length must be at least 8");
            return;
        }
        if (pass1.equals(pass2)) {
            pM.changeParentPassword(new String(newPassword.getPassword()));
            warnLabel.setText("Password changed");
        } else if (!pass1.equals(pass2)) {
            warnLabel.setText("Passwords don't match");
        }
    }//GEN-LAST:event_okButtonActionPerformed

